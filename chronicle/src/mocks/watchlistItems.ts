function priceHistory(base: number, trend: number): number[] {
  const result: number[] = []
  let price = base * (1 - trend * 0.15)
  for (let i = 0; i < 30; i++) {
    price = price * (1 + (Math.random() - 0.48) * 0.02 + (trend / 30) * 0.005)
    result.push(Math.round(price * 100) / 100)
  }
  return result
}


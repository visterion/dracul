ALTER TABLE executor_position ADD COLUMN mfe_r             NUMERIC(18,6);
ALTER TABLE executor_position ADD COLUMN soft_confirm_count INTEGER NOT NULL DEFAULT 0;
ALTER TABLE executor_position ADD COLUMN exit_price        NUMERIC(18,6);
